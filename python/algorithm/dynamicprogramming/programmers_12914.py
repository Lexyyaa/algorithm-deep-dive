def solution(n):
    # 문제에서 나머지를 구하라고 했기 때문에 미리 저장
    MOD = 1234567

    # dp 리스트 생성
    # dp[i] = i칸까지 가는 방법의 수
    dp = [0] * (n + 1)

    # 0칸에 가는 방법
    # 아무것도 안 하는 방법 1가지
    dp[0] = 1

    # 1칸에 가는 방법
    # 1칸 점프 한 번 → 1가지
    dp[1] = 1

    # 2칸부터 n칸까지 하나씩 계산
    for i in range(2, n + 1):
        # i칸에 오는 방법은
        # (i-1)칸에서 1칸 온 경우 +
        # (i-2)칸에서 2칸 온 경우
        dp[i] = dp[i - 1] + dp[i - 2]

        # 숫자가 너무 커지지 않도록 나머지 처리
        dp[i] = dp[i] % MOD

    # n칸에 도달하는 방법 수 반환
    return dp[n]
